package session1.datatypes;

public class OperatorsDemo {

    public static void main(String[] args) {
        int a = 10, b = 5;

        // Arithmetic operators
        System.out.println(a + b);

        // Relational operators
        System.out.println(a > b);

        // Logical operators
        System.out.println(a > 5 && b < 10);
    }
}
